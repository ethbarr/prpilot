/**
 * Escapes a string for safe insertion into HTML via innerHTML.
 * Covers the five standard HTML entities: & < > " '
 */
export function escapeHtml(text: string): string {
  return text
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;')
    .replace(/'/g, '&#39;');
}
